package cs3500.reversi.provider.model;

/**
 * <h3>Direction Enumeration</h3>
 * Represents an Enumeration for the six directions a run of {@link GamePiece} can be. This
 *     would be going diagonally in four directions or horizontally in two directions.
 */
public enum Direction {
  HorizontalLeft,
  HorizonalRight,
  DiagonalLeftUp,
  DiagonalRightUp,
  DiagonalRightDown,
  DiagonalLeftDown;
}
